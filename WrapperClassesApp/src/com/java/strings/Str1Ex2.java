package com.java.strings;

public class Str1Ex2 {
// StringBuffer & StringBuilder
	public static void main(String[] args) {
		StringBuffer sb = new StringBuffer("Venugopal");
		System.out.println(sb);
		sb.append(" Arumuru");
		System.out.println(sb);
		
		StringBuilder  sbr = new StringBuilder("Wipro");
		System.out.println(sbr);
		sbr.append(" Java Training");
		System.out.println(sbr);
		
		
	}
}
