package com.loops;

public class nslp1 {

	public static void main(String[] args) {

		/*
		 * 1 2 3
		 * 1 2 3
		 * 1 2 3
		 */
		
		int x = 1;
		while(x<=3)
		{
			System.out.print(x + "  ");
			x++;
		}
		System.out.println(); // break line
		x = 1;
		while(x<=3)
		{
			System.out.print(x + "  ");
			x++;
		}
		System.out.println(); // break line
		x = 1;
		while(x<=3)
		{
			System.out.print(x + "  ");
			x++;
		}

	}

}
